// 트리 구조로 변환하는 함수
export function buildTreeStructure(flatData) {
  const dataMap = {};
  const tree = [];

  // 먼저 모든 항목을 맵에 저장
  flatData.forEach(item => {
    dataMap[item.id] = { ...item, children: [] };
  });

  // 부모-자식 관계 구성
  flatData.forEach(item => {
    if (item.parentId === null) {
      tree.push(dataMap[item.id]);
    } else if (dataMap[item.parentId]) {
      dataMap[item.parentId].children.push(dataMap[item.id]);
    }
  });

  return tree;
}

